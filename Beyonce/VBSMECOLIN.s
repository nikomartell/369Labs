    slt $t0, $s7, $s6          # $t0 = (right < left)
    bne $t0, $zero, next_spiral_loop   # If $t0 is 1 (meaning left > right), skip the code

    # Save current i (in $s5) on the stack
    addi    $sp, $sp, -4           # Decrement stack pointer
    sw      $s5, 0($sp)            # Save bottom to stack

left_row_loop_start:
    # Check if i (in $s5) is less than top (in $s4)
    slt $t1, $s5, $s4                  # $t1 = 1 if (i < top)
    bne $t1, $zero, left_row_loop_end  # If $t1 is 0 (meaning i >= top), continue the loop

    # Calculate i + windowRows - 1
    add $t0, $s5, $s2              # $t0 = i + windowRows
    addi $t0, $t0, -1              # $t0 = i + windowRows - 1

    # Check if i + windowRows - 1 < rows
    slt $t1, $t0, $s0              # $t1 = (i + windowRows - 1 < rows)

    # Calculate left + windowCols - 1
    add $t2, $s6, $s3              # $t2 = left + windowCols
    addi $t2, $t2, -1              # $t2 = left + windowCols - 1

    # Check if left + windowCols - 1 < cols
    slt $t3, $t2, $s1              # $t3 = (left + windowCols - 1 < cols)

    # Check if i + windowRows - 1 < rows && left + windowCols - 1 < cols
    and $t4, $t1, $t3              # $t4 = $t1 && $t3
    beq $t4, $zero, left_row_loop_next   # If $t1 and $t3 are either false, move to left_row_loop_next
    
    addi $t0, $zero, 0                      # Initialize sad = 0
    addi $t1, $zero, -1                     # Initialize wi = 0
    
left_wi_loop_start:
    addi $t1, $t1, 1 # Increment wi++
    
    # Check if wi < windowRows
    slt $t3, $t1, $s2                # $t3 = (wi < windowRows)
    beq $t3, $zero, left_store_sad   # If not, exit the loop
    addi $t2, $zero, -1              # Initialize wj = 0

left_wj_loop_start:
    addi $t2, $t2, 1 # Increment wj++

    # Check if wj < windowCols
    slt $t3, $t2, $s3              # $t3 = (wj < windowCols)
    beq $t3, $zero, left_wi_loop_start     # If not, exit the loop

    # Calculate frameValue
    add $t3, $s5, $t1              # $t3 = i + wi
    mul $t3, $t3, $s1              # $t3 = (i + wi) * cols
    add $t3, $t3, $s6              # $t3 = (i + wi) * cols + left
    add $t3, $t3, $t2              # $t3 = (i + wi) * cols + left + wj
    sll $t3, $t3, 2                # $t3 = address offset (4 bytes per int)
    add $t3, $t3, $a1              # $t3 = address of frameValue
    lw $t4, 0($t3)                 # $t4 = frameValue

    # Calculate windowValue
    mul $t5, $t1, $s3              # $t5 = wi * windowCols
    add $t5, $t5, $t2              # $t5 = wi * windowCols + wj
    sll $t5, $t5, 2                # $t5 = address offset (4 bytes per int)
    add $t5, $t5, $a2              # $t5 = address of windowValue
    lw $t6, 0($t5)                 # $t6 = windowValue

    # Calculate abs(frameValue - windowValue)
    sub $t4, $t4, $t6              # $t4 = frameValue - windowValue
    slt $t3, $t4, $zero            # Set $t3 to 0 if $t4 < 0
    beq $t3, $zero left_wj_loop_end   # If $t4 < 0, make positive
    sub $t4, $zero, $t4            # $t4 = -$t4 (make it positive)                 
    
left_wj_loop_end:
    add $t0, $t0, $t4          # sad += abs(frameValue - windowValue)
    j left_wj_loop_start       # Jump back to the beginning of the loop

left_store_sad:
    slt $t3, $t0, $t9                   # $t1 = (sad < lowestDiff)
    beq $t3, $zero, left_row_loop_next  # If sad > lowestDiff, branch to left_row_loop_next

left_update_values:
    addi $t9, $t0, 0                  # lowestDiff = sad
    addi $v0, $s5, 0                  # bestI = i
    addi $v1, $s6, 0                  # bestJ = left

    beq $t9, $zero, end_while

left_row_loop_next:
    addi $s5, $s5, -1                    # Decrement i (bottom)
    j left_row_loop_start

left_row_loop_end:
    # Recover i (bottom) from the stack
    lw      $s5, 0($sp)               # Restore bottom from stack
    addi    $sp, $sp, 4               # Increment stack pointer to clean up
    
    # After processing the left row, increment left
    addi    $s6, $s6, 1              # left++

next_spiral_loop:
    j       while_loop               # Jump back to the start of the loop

end_while:
    jr      $ra                      # Return from subroutine

